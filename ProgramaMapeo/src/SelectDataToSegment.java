    private void eventoNSegmentos(){
        try {
            int newS = Integer.valueOf(jTextField2.getText());
            int newwxh = Integer.valueOf(jTextField3.getText());
            if(newS == nSegmentos) return;
            int newP = newwxh/newS;
            if(log2(newP)%1 == 0){ //solo permitiremos pixeles que sean potencia de 2
                //Actualizamos el número de píxeles
                jTextField1.setText(Integer.toString(newP));
            }else{
                jTextField1.setText(Integer.toString(newP));
                JOptionPane.showMessageDialog(null,"Los pixeles tienen que ser un nº potencia de 2",
                            "ERROR",ERROR_MESSAGE);
                jTextField1.setText(Integer.toString(pixeles));
                jTextField2.setText(Integer.toString(nSegmentos));
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"El número de segmentos que ser un nº entero",
                            "ERROR",ERROR_MESSAGE);
            jTextField1.setText(Integer.toString(pixeles));
            jTextField2.setText(Integer.toString(nSegmentos));
        }
    }
    
    private void eventoTamaño(){
        try {
            int newwxh = Integer.valueOf(jTextField3.getText());
            if(newwxh == wxh) return;
            if(newwxh == 2240 || newwxh == 2048){ //solo permitiremos que el tamaño sea o 2240 o 2048
                int newS = newwxh/Integer.valueOf(jTextField1.getText());
                jTextField2.setText(Integer.toString(newS));
            }else{
                JOptionPane.showMessageDialog(null,"El tamaño solo puede ser o 2240 0 2048",
                            "ERROR",ERROR_MESSAGE);
                jTextField3.setText(Integer.toString(wxh));
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"El número tiene que ser un nº entero",
                            "ERROR",ERROR_MESSAGE);
            jTextField3.setText(Integer.toString(wxh));
        }
    }
    
