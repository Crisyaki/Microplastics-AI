    private void eventoPixel(){
        try {
            int newP = Integer.valueOf(jTextField1.getText());
            int newwxh = Integer.valueOf(jTextField3.getText());
            if(newP == pixeles) return;
            int newS = newwxh/newP;
            if(log2(newP)%1 == 0){ //solo permitiremos pixeles que sean potencia de 2
                //Actualizamos los segmentos
                jTextField2.setText(Integer.toString(newS));
            }else{
                JOptionPane.showMessageDialog(null,"Tiene que ser un nº potencia de 2",
                            "ERROR",ERROR_MESSAGE);
                jTextField1.setText(Integer.toString(pixeles));
                jTextField2.setText(Integer.toString(nSegmentos));
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"Tiene que ser un nº entero",
                            "ERROR",ERROR_MESSAGE);
            jTextField1.setText(Integer.toString(pixeles));
            jTextField2.setText(Integer.toString(nSegmentos));
        }
    }
    
    private static double log2(int n){
        return (Math.log(n) / Math.log(2));
    }
    
